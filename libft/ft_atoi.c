#include "libft.h"

int		ft_atoi(const char *str)
{
	int a;
	int i;

	a = 1;
	i = 0;
	while ((*str == ' ') || (*str == '\t') || (*str == '\n')
			|| (*str == '\v') || (*str == '\r') || (*str == '\f'))
		str++;
	if (*str == '-' && *(str + 1) >= '0' && *(str + 1) <= '9')
	{
		a = -1;
		str++;
	}
	else if (*str == '+' && *(str + 1) >= '0' && *(str + 1) <= '9')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		i = i * 10 + *str - '0';
		str++;
	}
	return (i * a);
}
