#include "libft.h"

int				ft_max(int *s, size_t len)
{
	size_t		a;
	int			buff;

	a = 1;
	while (a < len)
	{
		if (s[a] < s[a - 1])
		{
			buff = s[a];
			s[a] = s[a - 1];
			s[a - 1] = buff;
			a = 1;
		}
		else
			a++;
	}
	return (s[len - 1]);
}
