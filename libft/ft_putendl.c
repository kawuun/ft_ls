#include "libft.h"

void		ft_putendl(char const *s)
{
	int		a;

	if (!s)
		return ;
	a = 0;
	while (s[a] != '\0')
	{
		write(1, &s[a], 1);
		a++;
	}
	write(1, "\n", 1);
}
