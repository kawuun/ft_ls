#include "libft.h"

int			ft_strequ(char const *s1, char const *s2)
{
	int		a;

	a = 0;
	if ((!s1 && s2) || (!s2 && s1) || (!s1 && !s2))
		return (0);
	while (s1[a] != '\0')
	{
		if (s1[a] != s2[a])
			return (0);
		else
			a++;
	}
	if (s2[a] == '\0')
		return (1);
	else
		return (0);
}
